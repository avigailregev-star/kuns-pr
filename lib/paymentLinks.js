/**
 * מחזיר טיירים של מחירים לפי שם קורס.
 * until18 = עד 1.8, until19 = עד 1.9, regular = מחיר רגיל אחרי 1.9
 */
export function getCoursePriceTiers(courseName) {
  if (!courseName) return null;
  const n = courseName.replace(/[''`´]/g, "'").trim();

  // קורסים ללא הנחה
  if (n === 'הזמיר') return { regular: 1500 };
  if (n.includes("שנה ב'")) return { until18: 1200, until19: 1500, regular: 1750 };
  if (n.includes("שנה ג'")) return { until18: 1500, until19: 1750, regular: 2250 };
  if (n.includes("שנה ד'")) return { until18: 2750, until19: 3000, regular: 3500 };

  // שיעורים קבוצתיים — הנחה עד 1.9 בלבד
  if (/DJ/.test(n)) return { until19: 2750, regular: 3000 };
  if (/CUBASE|קיובייס|הפקה יוצרת/.test(n)) return { until19: 2250, regular: 2500 };
  if (/מדברנא/.test(n)) return { until19: 650, regular: 1000 };
  if (/מקהלת צעירים/.test(n)) return { until19: 500, regular: 750 };
  if (/תזמורת|מקהלה|תיאוריה|אנסמבל/.test(n)) return null;

  // פיתוח קול זוגי
  if (n.includes('זוגי')) return { until18: 2500, until19: 2800, regular: 3000 };

  // מוזיקה מן המזרח — 45 דק' עם מחיר ביניים שונה
  if (/^עוד|^בגלמה|^קמנצ/.test(n) && n.includes('45 ד')) {
    return { until18: 3800, until19: 4000, regular: 4600 };
  }

  // שיעורים אישיים רגילים
  if (n.includes('45 ד')) return { until18: 3800, until19: 4200, regular: 4600 };
  if (n.includes('60 דק')) return { until18: 4200, until19: 4600, regular: 5000 };

  return null;
}

/**
 * מחזיר את המחיר הנוכחי לפי תאריך היום ומידע על הנחה.
 * { currentPrice, regularPrice, isDiscounted, discountLabel }
 */
export function getCurrentPriceInfo(courseName) {
  const tiers = getCoursePriceTiers(courseName);
  if (!tiers) return null;

  const now = new Date();
  const year = now.getFullYear();
  const aug1 = new Date(year, 7, 1);
  const sep1 = new Date(year, 8, 1);

  let currentPrice;
  let discountLabel = null;

  if (tiers.until18 && now < aug1) {
    currentPrice = tiers.until18;
    discountLabel = 'הרשמה מוקדמת עד 1.8';
  } else if (tiers.until19 && now < sep1) {
    currentPrice = tiers.until19;
    discountLabel = 'הרשמה מוקדמת עד 1.9';
  } else {
    currentPrice = tiers.regular;
  }

  return {
    currentPrice,
    regularPrice: tiers.regular,
    isDiscounted: currentPrice < tiers.regular,
    discountLabel,
  };
}

/**
 * מחזיר מחיר מוצג לפי שם קורס, או null אם לא ידוע.
 */
export function getCoursePrice(courseName) {
  const info = getCurrentPriceInfo(courseName);
  if (!info) return null;
  return `${info.currentPrice.toLocaleString()} ₪ לשנה`;
}
